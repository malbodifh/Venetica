/* ── Idioma activo ── */
let lang = localStorage.getItem('lang') || 'es';

function applyLang(l) {
  lang = l;
  localStorage.setItem('lang', l);
  document.getElementById('htmlRoot').lang = l;
  document.title = T[l].page_title;

  const btnLabel = l === 'es' ? 'EN' : 'ES';
  document.getElementById('langBtn').textContent = btnLabel;
  const mob = document.getElementById('langBtnMob');
  if(mob) mob.textContent = btnLabel;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const v = T[l][el.dataset.i18n];
    if(v !== undefined) el.textContent = v;
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const v = T[l][el.dataset.i18nHtml];
    if(v !== undefined) el.innerHTML = v;
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const v = T[l][el.dataset.i18nPh];
    if(v !== undefined) el.placeholder = v;
  });

  updateProject();
}

document.getElementById('langBtn').addEventListener('click', () => applyLang(lang === 'es' ? 'en' : 'es'));
