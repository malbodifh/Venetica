emailjs.init(EMAILJS_PUBLIC_KEY);

const contactForm  = document.getElementById('contactForm');
const formMsg      = document.getElementById('formMsg');
const formSubmit   = document.getElementById('formSubmit');
const submitSpan   = formSubmit.querySelector('[data-i18n="con_submit"]');

function showMsg(type, html) {
  formMsg.innerHTML  = html;
  formMsg.className  = 'form-msg ' + type;
  formMsg.style.display = 'block';
  formMsg.scrollIntoView({behavior:'smooth', block:'nearest'});
}

contactForm.addEventListener('submit', async e => {
  e.preventDefault();

  const nombre = document.getElementById('f_name').value.trim();
  const email  = document.getElementById('f_email').value.trim();

  if(!nombre || !email) {
    showMsg('error', lang === 'es'
      ? 'Por favor completá tu nombre y email.'
      : 'Please fill in your name and email.');
    return;
  }

  formSubmit.disabled = true;
  submitSpan.textContent = T[lang].form_sending;
  formMsg.style.display  = 'none';

  try {
    await emailjs.sendForm(EMAILJS_SERVICE_ID, EMAILJS_TEMPLATE_ID, contactForm);

    const waMsg = encodeURIComponent(lang === 'es'
      ? `Hola, soy ${nombre}. Les contacto desde el sitio web de Venetica Constructora.`
      : `Hello, I'm ${nombre}. I'm contacting you from the Venetica Constructora website.`
    );
    const waUrl = `https://wa.me/${WA_PHONE}?text=${waMsg}`;

    showMsg('success',
      `${T[lang].form_success}<br>
       <a class="form-wa-link" href="${waUrl}" target="_blank" rel="noopener">
         <svg viewBox="0 0 18 18" fill="none" width="14" height="14"><path d="M9 2C5.69 2 3 4.69 3 8c0 1.07.29 2.08.8 2.94L3 15l4.18-.76A6 6 0 009 14c3.31 0 6-2.69 6-6S12.31 2 9 2z" stroke="currentColor" stroke-width="1.4"/></svg>
         ${T[lang].form_wa_btn}
       </a>`
    );
    contactForm.reset();

  } catch(err) {
    showMsg('error', T[lang].form_error);
  } finally {
    formSubmit.disabled = false;
    submitSpan.textContent = T[lang].con_submit;
  }
});

