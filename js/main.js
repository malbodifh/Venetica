function setPosition(p) {
  pct = Math.max(2, Math.min(98, p));
  if(rafId) return;
  rafId = requestAnimationFrame(() => {
    before.style.clipPath = `inset(0 ${100 - pct}% 0 0)`;
    handle.style.left = pct + '%';
    rafId = null;
  });
}

function getX(e) {
  const rect = slider.getBoundingClientRect();
  const x = e.touches ? e.touches[0].clientX : e.clientX;
  return ((x - rect.left) / rect.width) * 100;
}

slider.addEventListener('mousedown', e => { dragging = true; setPosition(getX(e)); e.preventDefault(); });
window.addEventListener('mousemove', e => { if(dragging) setPosition(getX(e)); });
window.addEventListener('mouseup',   () => { dragging = false; });
slider.addEventListener('touchstart', e => { dragging = true; setPosition(getX(e)); }, {passive:true});
window.addEventListener('touchmove',  e => { if(dragging) setPosition(getX(e)); }, {passive:true});
window.addEventListener('touchend',   () => { dragging = false; });

