/* ── Dots ── */
function buildDots() {
  dots.innerHTML = '';
  filteredProjects().forEach((_, i) => {
    const d = document.createElement('button');
    d.className = 'sn-dot' + (i === cur ? ' on' : '');
    d.onclick = () => go(i);
    dots.appendChild(d);
  });
}

/* ── Actualizar proyecto ── */
function updateProject() {
  const fp = filteredProjects();
  if(!fp.length) return;
  const p = fp[cur];

  titleEl.textContent = p['title_' + lang];
  metaEl.textContent  = p['meta_' + lang];

  tags.innerHTML = p['tags_' + lang]
    .map(t => `<span class="ba-tag">${t}</span>`)
    .join('');

  /* ba-before (capa de clip, lado izquierdo) muestra el DESPUÉS para que
     arrastrar a la derecha revele más del resultado terminado */
  before.innerHTML = `<img src="${p.after}"  alt="${T[lang].proj_after}">`;
  after.innerHTML  = `<img src="${p.before}" alt="${T[lang].proj_before}">`;

  setPosition(50);

  dots.querySelectorAll('.sn-dot')
    .forEach((d, i) => d.classList.toggle('on', i === cur));
}

/* ── Cambiar proyecto ── */
function go(i) {
  const fp = filteredProjects();
  cur = (i + fp.length) % fp.length;
  updateProject();
}

